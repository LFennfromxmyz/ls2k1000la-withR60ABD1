/**
 * @brief 显示屏初始化
 */
void DisplayInit(void)
{
	OLED_Init();
	//清屏处理
	memset(OLED_DATA_BUF, 0, sizeof(OLED_DATA_BUF));

	DisplayRefresh();
}
/**
 * @brief 显示屏刷新显示
 */
void DisplayRefresh(void)
{
	OLED_Diaplay(OLED_DATA_BUF);
	memset(OLED_DATA_BUF, 0, sizeof(OLED_DATA_BUF));
}

/**
 * @brief 画点函数
 * @param  x  横坐标
 * @param  y  竖坐标
 */
void Oled_Draw_Point(uint8_t x, uint8_t y)
{
	if (x < 128 && y < 64)
	{
		OLED_DATA_BUF[x][y / 8] |= (0x01 << (y % 8));
	}
}

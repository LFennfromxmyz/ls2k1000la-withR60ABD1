/**
 * @brief 滚动显示的底层支持
 */
static struct
{
	uint8_t len;
	uint8_t loc;
	uint8_t lastlen;
} ScrollDisplay;
/**
 * @brief 清除滚动显示的参数（一般在切换界面时使用）
 */
void OledScrollClear(void)
{
	ScrollDisplay.lastlen = 0;
	ScrollDisplay.len = 0;
	ScrollDisplay.loc = 0;
}

/**
 * @brief 滚动显示
 * @param  x   起始坐标x
 * @param  y   起始坐标y
 * @param  str  显示字符串
 * @param  len  字符串长度
 */
void OledScrollDisplay(uint8_t x, uint8_t y, char *str, uint8_t len)
{
	if (len < 16)
	{
		Oled_Disp_String(x, y, str);
	}
	else
	{
		if (ScrollDisplay.lastlen != len)
		{
			ScrollDisplay.loc = 0;
		}
		Oled_Disp_String(x, y, str + ScrollDisplay.loc);
		ScrollDisplay.len = strlen(str + ScrollDisplay.loc);
		str[ScrollDisplay.loc] = 0; //截止显示字符串
		if ((len - strlen(str)) < 16)
		{
			Oled_Disp_String(ScrollDisplay.len * 8, y, str);
		}

		ScrollDisplay.loc++;
		if (ScrollDisplay.loc >= len)
			ScrollDisplay.loc = 0;

		ScrollDisplay.lastlen = len;
	}
}

/**
 * @brief 导入曲线数据
 * @param  Draw   接收曲线数据缓存
 * @param  val    传入新的数据点  数据为0~1，按照百分比来传入
 */
void WriteCurveData(_DrawCurve *Draw, float val)
{ //传入百分比
	uint8_t i;
	for (i = 1; i < 128; i++)
	{
		Draw->Draw_Buf[i - 1] = Draw->Draw_Buf[i];
	}
	Draw->Draw_Buf[127] = val;
	if (Draw->Draw_Buf[127] > 1)
		Draw->Draw_Buf[127] = 1;
	else if (Draw->Draw_Buf[127] < 0)
		Draw->Draw_Buf[127] = 0;

	Draw->Draw_Max = 0;
	Draw->Draw_Min = 1;
	for (i = 0; i < 128; i++) //获取最大最小范围
	{
		if (Draw->Draw_Min > (Draw->Draw_Buf[i]))
		{
			Draw->Draw_Min = Draw->Draw_Buf[i];
			Draw->Draw_Min = (float)((uint8_t)(Draw->Draw_Min * 100) / 5) / 20;

			if (Draw->Draw_Min < 0)
				Draw->Draw_Min = 0;
			if ((Draw->Draw_Max - Draw->Draw_Min) < 0.05)
				Draw->Draw_Max = Draw->Draw_Min + 0.05;
		}
		else if (Draw->Draw_Max < Draw->Draw_Buf[i])
		{
			Draw->Draw_Max = Draw->Draw_Buf[i];
			Draw->Draw_Max = (float)((uint8_t)(Draw->Draw_Max * 100) / 5 + 1) / 20;
			if (Draw->Draw_Max > 1.0)
				Draw->Draw_Max = 1.0;
			if ((Draw->Draw_Max - Draw->Draw_Min) < 0.05)
				Draw->Draw_Min = Draw->Draw_Max - 0.05;
		}
	}
}
/**
 * @brief 打印曲线
 * @param  Draw   曲线数据
 * @param  fllor  曲线显示的底部
 * @param  upper  曲线显示的顶部
 */
void OledDrawCurve(_DrawCurve Draw, uint8_t fllor, uint8_t upper)
{
	uint8_t i = 0;

	for (i = 1; i < 128; i++) //画线
	{
		if ((Draw.Draw_Buf[i - 1] > Draw.Draw_Max) || (Draw.Draw_Buf[i - 1] < Draw.Draw_Min))
			continue;
		Display_Line(i - 1, upper - (Draw.Draw_Buf[i - 1] - Draw.Draw_Min) / (Draw.Draw_Max - Draw.Draw_Min) * (upper - fllor), i, upper - (Draw.Draw_Buf[i] - Draw.Draw_Min) / (Draw.Draw_Max - Draw.Draw_Min) * (upper - fllor));
	}
}
/**
 * @brief 字符串居中显示
 * @param  x  显示坐标x
 * @param  y  显示坐标y
 * @param  length   显示长度
 * @param  str   显示字符串
 */
void OledCenteredDisplayStr(uint8_t x, uint8_t y, uint8_t length, const char *str)
{
	Oled_Disp_String(x + (length - 8 * strlen(str)) / 2, y, str);
}
